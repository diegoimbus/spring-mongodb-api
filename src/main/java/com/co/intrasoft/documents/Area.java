package com.co.intrasoft.documents;

public class Area {
	
	private String areaName;
	private String city;
	
	public Area() {
	}

	public String getAreaName() {
		return areaName;
	}

	public void setAreaName(String areaName) {
		this.areaName = areaName;
	}

	public String getCity() {
		return city;
	}

	public void setCity(String city) {
		this.city = city;
	}

}
